/**
 * Bundle the on-disk Drizzle migrations into a single TS module the Cloudflare
 * Durable Object migrator can import (Workers can't read the filesystem). Run
 * after `drizzle-kit generate`:  node scripts/bundle-migrations.mjs
 *
 * Output shape matches drizzle-orm/durable-sqlite's MigrationConfig:
 *   { journal, migrations: { m0000: "<sql>", m0001: "<sql>", ... } }
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const here = path.dirname(fileURLToPath(import.meta.url));
const migrationsDir = path.join(here, "..", "migrations");
const journal = JSON.parse(readFileSync(path.join(migrationsDir, "meta", "_journal.json"), "utf8"));

const migrations = {};
for (const entry of journal.entries) {
  const sql = readFileSync(path.join(migrationsDir, `${entry.tag}.sql`), "utf8");
  migrations[`m${String(entry.idx).padStart(4, "0")}`] = sql;
}

const out =
  `// AUTO-GENERATED by scripts/bundle-migrations.mjs — do not edit by hand.\n` +
  `// Regenerate after every \`drizzle-kit generate\`.\n` +
  `export const migrationBundle = ${JSON.stringify({ journal, migrations }, null, 2)} as const;\n`;

writeFileSync(path.join(here, "..", "src", "migrations.bundle.ts"), out);
console.log(`bundled ${journal.entries.length} migrations -> src/migrations.bundle.ts`);
